// ---------------------------------------------------------------------------
// Tag helpers
// ---------------------------------------------------------------------------

struct FixtureTags {
    modality: &'static str,
    manufacturer: &'static str,
    model_name: &'static str,
    image_type: &'static str,
    series_description: &'static str,
    rows: u16,
    cols: u16,
    sop_instance_uid: &'static str,
}

fn put_str(
    obj: &mut FileDicomObject<dicom_object::InMemDicomObject>,
    tag: dicom_core::Tag,
    vr: VR,
    value: &str,
) {
    obj.put(DataElement::new(
        tag,
        vr,
        Value::Primitive(PrimitiveValue::from(value)),
    ));
}

fn put_u16(
    obj: &mut FileDicomObject<dicom_object::InMemDicomObject>,
    tag: dicom_core::Tag,
    vr: VR,
    value: u16,
) {
    obj.put(DataElement::new(
        tag,
        vr,
        Value::Primitive(PrimitiveValue::from(value)),
    ));
}

fn set_common_tags(obj: &mut FileDicomObject<dicom_object::InMemDicomObject>, t: &FixtureTags) {
    put_str(obj, tags::MODALITY, VR::CS, t.modality);
    put_str(obj, tags::MANUFACTURER, VR::LO, t.manufacturer);
    put_str(obj, tags::MANUFACTURER_MODEL_NAME, VR::LO, t.model_name);
    put_str(obj, tags::IMAGE_TYPE, VR::CS, t.image_type);
    if !t.series_description.is_empty() {
        put_str(obj, tags::SERIES_DESCRIPTION, VR::LO, t.series_description);
    }

    // Pixel format: 8-bit monochrome
    put_u16(obj, tags::ROWS, VR::US, t.rows);
    put_u16(obj, tags::COLUMNS, VR::US, t.cols);
    put_u16(obj, tags::BITS_ALLOCATED, VR::US, 8);
    put_u16(obj, tags::BITS_STORED, VR::US, 8);
    put_u16(obj, tags::HIGH_BIT, VR::US, 7);
    put_u16(obj, tags::PIXEL_REPRESENTATION, VR::US, 0);
    put_u16(obj, tags::SAMPLES_PER_PIXEL, VR::US, 1);
    put_str(obj, tags::PHOTOMETRIC_INTERPRETATION, VR::CS, "MONOCHROME2");

    // Tags required by the recipe's %header section
    put_str(obj, tags::PATIENT_NAME, VR::PN, "TEST^PATIENT");
    put_str(obj, tags::PATIENT_ID, VR::LO, "TEST-ID-001");
    put_str(obj, tags::STUDY_DATE, VR::DA, "20240101");
    put_str(obj, tags::SOP_INSTANCE_UID, VR::UI, t.sop_instance_uid);
    put_str(obj, tags::STUDY_INSTANCE_UID, VR::UI, "1.2.3.4.5.6.7.100");
    put_str(obj, tags::SERIES_INSTANCE_UID, VR::UI, "1.2.3.4.5.6.7.200");
}

