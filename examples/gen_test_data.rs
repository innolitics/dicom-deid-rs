fn gen_uncompressed(filename: &str, t: FixtureTags) {
    let meta = FileMetaTableBuilder::new()
        .transfer_syntax("1.2.840.10008.1.2.1") // Explicit VR Little Endian
        .media_storage_sop_class_uid("1.2.840.10008.5.1.4.1.1.2") // CT Image Storage
        .media_storage_sop_instance_uid(t.sop_instance_uid)
        .implementation_class_uid("1.2.3.4")
        .build()
        .expect("valid file meta");

    let mut obj = FileDicomObject::new_empty_with_meta(meta);
    set_common_tags(&mut obj, &t);

    // All-white pixel data
    let pixel_count = (t.rows as usize) * (t.cols as usize);
    let pixels = vec![0xFFu8; pixel_count];
    obj.put(DataElement::new(
        tags::PIXEL_DATA,
        VR::OW,
        Value::Primitive(PrimitiveValue::U8(pixels.into())),
    ));

    let path = format!("tests/fixtures/graylist/{}", filename);
    obj.write_to_file(&path).expect("should write DICOM file");
    println!("Created {path}");
}

fn gen_j2k_compressed(filename: &str, t: FixtureTags, j2k_data: &[u8]) {
    let ts = "1.2.840.10008.1.2.4.90"; // JPEG 2000 Lossless

    // Use US Multi-frame Image Storage for US, CT Image Storage for CT
    let sop_class = if t.modality == "US" {
        "1.2.840.10008.5.1.4.1.1.3.1" // US Multi-frame Image Storage
    } else {
        "1.2.840.10008.5.1.4.1.1.2" // CT Image Storage
    };

    let meta = FileMetaTableBuilder::new()
        .transfer_syntax(ts)
        .media_storage_sop_class_uid(sop_class)
        .media_storage_sop_instance_uid(t.sop_instance_uid)
        .implementation_class_uid("1.2.3.4")
        .build()
        .expect("valid file meta");

    let mut obj = FileDicomObject::new_empty_with_meta(meta);
    set_common_tags(&mut obj, &t);

    // Encapsulated pixel data (JPEG 2000 codestream as single fragment)
    let pixel_data = DataElement::new(
        tags::PIXEL_DATA,
        VR::OB,
        Value::PixelSequence(dicom_core::value::PixelFragmentSequence::new(
            vec![0u32],
            vec![j2k_data.to_vec()],
        )),
    );
    obj.put(pixel_data);

    let path = format!("tests/fixtures/graylist/{}", filename);
    obj.write_to_file(&path).expect("should write DICOM file");
    println!("Created {path}");
}

